public class DeklarasiVariabel {
    public static void main(String[] args) {
        
        //Deklarasi variabel integer bernama 'StrukturBaris'
        int StrukturBaris;

        //Inisialisasi (memberi nilai awal)pada variabel StrukturBaris
        StrukturBaris = 100; //merupakan contoh nilai yang saya ambil

        //Kemudian Deklarasi variabel string bernama 'KataBaru'
        String KataBaru = "Deklarasi tipe data String ";

        //Terakhir, menampilkan nama variabel
        System.out.println("Nilai StrukturBaris:" + StrukturBaris);
        System.out.println("Nilai KataBaru:" + KataBaru);

    }
}
